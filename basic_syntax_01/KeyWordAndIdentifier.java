class KeyWordAndIdentifier{
	public static void main(String[] args){
		//变量


		int a=5;
		int b=3;
		//加
		System.out.println(a+b);
		//减
		System.out.println(a-b);
		//乘
		System.out.println(a*b);
		//除法
		System.out.println(a/3.0);
		//整除
		System.out.println(a/b);
		//求余数
		System.out.println(a%b);

		/*标示符
		由大小写英文字符、数字、下划线组成，不能以数字开头
		用于Java中各种东西的名称，比如类名、方法名、变量名
		类名：大驼峰，方法名：小驼峰，变量名：
		标示符区分大小写
		*/

		/*关键字是java语法的保留字，不能用于名字
		public
		class
		static
		void
		int
		文本编辑器会自动高亮
		*/

	}
}
